print("5 != 6 ? ", 5 != 6)
print("3 < 4 ? ", 3 < 4)
print("4 <= 4 ? ", 4 <= 4)
print("100 > 200 ? ", 100 > 200)
print("12 >= 14 ? ", 12 >= 14)
